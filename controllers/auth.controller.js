import {
  authenticateUser,
  clearUserSession,
  comparePassword,
  createUser,
  getUserByEmail,
  hashPassword,
} from "../services/auth.services.js";
import {
  loginUserSchema,
  registerUserSchema,
} from "../validators/auth-validator.js";

export const getRegisterPage = (req, res) => {
  if (req.user) return res.redirect("/");     // if user is already logged in then redirect to home page if he try to access register page
  return res.render("../views/auth/register", { errors: req.flash("errors") });   // render register page with any flash errors if exists
};

// when the user register and click on register button
export const postRegister = async (req, res) => {
  if (req.user) return res.redirect("/");   // if user is already logged in then redirect to home page if he try to access register page
  const { data, error } = registerUserSchema.safeParse(req.body); // validate the req.body using zod schema

  if (error) {
    const errorMessage = error.errors[0].message;
    req.flash("errors", errorMessage);
    return res.redirect("/");
  }

  const { name, email, password } = data;

  const userExists = await getUserByEmail(email);
  console.log("userExists ", userExists);

  if (userExists) {                       // if user already exists with the same email
    req.flash("errors", "User already exists");
    return res.redirect("/register");
  }

  const hashedPassword = await hashPassword(password);

  const [user] = await createUser({ name, email, password: hashedPassword });     // create user in db // userID will be auto generated by db
  console.log("user register", user);

  // res.redirect("/login");

  await authenticateUser({ req, res, user, name, email });    // automatically login the user after registration

  res.redirect("/");
};

export const getLoginPage = (req, res) => {
  if (req.user) return res.redirect("/");         // if user is already logged in then redirect to home page if he try to access login page

  return res.render("auth/login", { errors: req.flash("errors") });  // render login page with any flash errors if exists
};

export const postLogin = async (req, res) => {
  if (req.user) return res.redirect("/");

  // const { email, password } = req.body;
  const { data, error } = loginUserSchema.safeParse(req.body);    

  if (error) {
    const errorMessage = error.errors[0].message;
    req.flash("errors", errorMessage);
    return res.redirect("/");
  }

  const { email, password } = data;

  const user = await getUserByEmail(email);
  console.log("user email", user);

  if (!user) {
    req.flash("errors", "Invalid Email or Password");
    return res.redirect("/login");
  }

  const isPasswordValid = await comparePassword(password, user.password);

  if (!isPasswordValid) {
    req.flash("errors", "Invalid email or password");
    return res.redirect("/login");
  }
  // we need to create a sessions
  await authenticateUser({ req, res, user });

  res.redirect("/");
};

